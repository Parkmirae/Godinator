public class VisitDto {

	private String vistiDate;	 // 방문일자
	private String vistiCnt;	 // 방문자수

	public String getVistiDate() {
		return vistiDate;
	}

	public void setVistiDate(String vistiDate) {
		this.vistiDate = vistiDate;
	}

	public String getVistiCnt() {
		return vistiCnt;
	}

	public void setVistiCnt(String vistiCnt) {
		this.vistiCnt = vistiCnt;
	}

}
